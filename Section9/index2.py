#slicing تقطيع

# A list of names 
names = ['Ahmed', 'Amr', 'Hesham', 'KhaLed', 'Zeyad']

#هبدأ تقطيع من القيمة صفر لحد 2 و قيمة 2 مش هتكون معايا مهم دي 
print(names[0:2])

# طريقة أخري
# أنهي عند القيمة الخامس عشان احط القيمة الأخيرة اللي عندي
print(names[:5])

# هبدا من القيمة 0 
print(names[0:])

# start at 0 and end in -1
print(names[:-1])
