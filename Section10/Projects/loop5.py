fruits = ['apple', 'banana', 'orange', 'grape', 
'kiwi', 'mango', 'strawberry', 'pineapple']

index= 0

# use a while loop to print every item in list
# boolean expersion False when index >> 8
while index < len(fruits):
    # print the current value item
    print(fruits[index])
    # زود واحد
    index += 1